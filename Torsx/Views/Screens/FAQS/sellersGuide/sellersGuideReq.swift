                    Ask your TORSX broker to provide you with information about financing, estimated monthly mortgage payments, and any other necessary qualifications. Pre-approval letters make for strong offers because the lender has already pulled a credit report, checked debt/to/income ratio and done an analysis of your finances. Also, it is preferable to be pre-approved so that there will be no surprises when the credit report is received.
